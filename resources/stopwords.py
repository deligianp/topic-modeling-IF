import math


def is_stopword(word_to_check, low_index=-1, length=-1):
    if low_index == -1:
        low_index = 0
    if length == -1:
        length = len(stopwords)
    median_index = low_index + int(length / 2)
    median = stopwords[median_index]
    if word_to_check < median:
        sublist_length = math.ceil((length - 1) / 2)
        if sublist_length != 0:
            return is_stopword(word_to_check, low_index, sublist_length)
        else:
            return False
    elif word_to_check > median:
        sublist_length = math.floor((length - 1) / 2)
        if sublist_length != 0:
            return is_stopword(word_to_check, median_index + 1, sublist_length)
        else:
            return False
    else:
        return True
